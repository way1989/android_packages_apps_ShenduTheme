package com.shendu.theme.ben;

public class ModuleSelecet {
	String title;
	String name;
	public ModuleSelecet(String title,String name){
		this.title = title;
		this.name = name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
